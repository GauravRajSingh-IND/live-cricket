import tkinter

data = {
    "Number_Matches": 5,
    "date": [
        "Thu, 19 Sep 2024 - Mon, 23 Sep 2024",
        "Fri, 27 Sep 2024 - Tue, 01 Oct 2024",
        "Sun, 06 Oct 2024",
        "Wed, 09 Oct 2024",
        "Sat, 12 Oct 2024",
    ],
    "match_id": [100220, 100229, 100238, 100247, 100256],
    "match_description": ["1st Test", "2nd Test", "1st T20I", "2nd T20I", "3rd T20I"],
    "match_format": ["TEST", "TEST", "T20", "T20", "T20"],
    "match_state": ["Stumps", "Upcoming", "Upcoming", "Upcoming", "Upcoming"],
    "match_status": [
        "Day 3: Stumps - Bangladesh need 357 runs",
        "Match starts at Sep 27, 04:00 GMT",
        "Match starts at Oct 06, 13:30 GMT",
        "Match starts at Oct 09, 13:30 GMT",
        "Match starts at Oct 12, 13:30 GMT",
    ],
}


class Test:

    def __init__(self):
        self.series_window = tkinter.Tk()
        self.series_window.title("Testing")
        self.series_window.geometry("1200x800")
        self.series_window.config(bg="lavender")

        self.series_matches_data = data

        # add number of matches.
        self.series_window_noMatch = tkinter.Label(self.series_window,
                                                   text= f"Number of Matches: {self.series_matches_data['Number_Matches']}",
                                                   font= ('arial', 20, 'bold'), background= "lavender" )
        self.series_window_noMatch.place(x=50, y=100)

        # create a canvas.
        self.series_canvas = tkinter.Canvas(self.series_window, width= 600, height= 600)

        x = 70
        y = 20
        for i in range(len(self.series_matches_data["match_id"])):
            #print(self.series_matches_data["match_id"][i])
            self.series_canvas.create_text(x, y, text= f"Match ID:{self.series_matches_data["match_id"][i]}",
                                           font= ('arial', 15, 'bold'))
            self.series_canvas.create_text(x+400, y, text=f"{self.series_matches_data["date"][i]}",
                                           font=('arial', 10, 'bold'))
            self.series_canvas.create_text(x + 70, y + 50, text=f"Status:{self.series_matches_data["match_description"][i]} - {self.series_matches_data["match_state"][i]}",
                                           font=('arial', 20, 'bold'))

            self.series_canvas.create_text(x + 370, y + 50,
                                           text=f"{self.series_matches_data["match_status"][i]}",
                                           font=('arial', 15, 'bold'))
            y += 120
        self.series_canvas.place(x=50, y=150)

    def exit(self):
        self.series_window.mainloop()

test = Test()
test.exit()